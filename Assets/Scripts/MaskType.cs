/// <summary>
/// Enum defining the 3 mask types in the game
/// Corresponds to keys 1, 2, 3
/// </summary>
public enum MaskType
{
    NONE = -1,          // No mask worn
    HONESTY = 0,        // Mask 1 (Key 1) - White/Gray - Honest, direct
    KINDNESS = 1,       // Mask 2 (Key 2) - Warm yellow - Kind, empathetic
    INDIFFERENCE = 2    // Mask 3 (Key 3) - Dark blue/purple - Cold, distant
}
